$(document).ready(function() {
  var animal = prompt("Do you want to learn about turtles, snakes or insects?");

  if (animal === "turtles") {
    $('#turtles').show();
  } else if (animal === "turtle"){
    $('#turtles').show();
  } else if (animal === "snakes"){
    $('#snakes').show();
  } else if (animal === "snake"){
    $('#snakes').show();
  } else if (animal === "insects"){
    $('#insects').show();
  } else if (animal === "insect"){
    $('#insects').show();
  }

});
